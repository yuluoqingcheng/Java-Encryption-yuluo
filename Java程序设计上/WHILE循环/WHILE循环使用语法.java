public class Main {
    public static void main(String[] args) {
       int i=0;
       // “//”为注释，运行时会忽略不运行
       // (i<x, x代表打印几遍)
       while(i<5){
        System.out.print("我爱JAVA");
        // i++等于i=i+1, i--等于i=i-1, i++为自增， i--为自减
        i=i+1;
       }
       // 上面程序结束时会运行打印出这句话
       System.out.println("程序结束"); 
       // 此段代码由Chian-小白提交，并由雨落倾城上传，未得允许禁止商业用于（虽然不太可能）
    }
}